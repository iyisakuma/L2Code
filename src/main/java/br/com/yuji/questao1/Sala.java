package br.com.yuji.questao1;

public class Sala {

    private int largura;
    private int comprimento;

    public Sala(int largura, int comprimento){
        this.largura = largura;
        this.comprimento = comprimento;
    }

    public int getLargura() {
        return largura;
    }

    public int getComprimento() {
        return comprimento;
    }
}
